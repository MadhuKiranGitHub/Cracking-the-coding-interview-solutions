package recursion;

public class Point{
	public int x;
	public int y;
 
	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

	public Point(int xcod, int ycod){
		this.x=xcod;
		this.y=ycod;
	}

}
